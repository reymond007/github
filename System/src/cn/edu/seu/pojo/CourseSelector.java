package cn.edu.seu.pojo;

public class CourseSelector {
	private long selectorId;
	private long studentNumber;
	private long lessonNumber;
	private Student student;
	private Glesson glesson;
	public Glesson getGlesson() {
		return glesson;
	}
	public void setGlesson(Glesson glesson) {
		this.glesson = glesson;
	}
	public long getSelectorId() {
		return selectorId;
	}
	public void setSelectorId(long selectorId) {
		this.selectorId = selectorId;
	}
	public long getStudentNumber() {
		return studentNumber;
	}
	public void setStudentNumber(long studentNumber) {
		this.studentNumber = studentNumber;
	}
	public long getLessonNumber() {
		return lessonNumber;
	}
	public void setLessonNumber(long lessonNumber) {
		this.lessonNumber = lessonNumber;
	}

	public Student getStudent() {
		return student;
	}
	public void setStudent(Student student) {
		this.student = student;
	}

}
